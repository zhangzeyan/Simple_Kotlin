package com.simple.kotlin.chapter2


val aBoolean: Boolean = true

val anInt: Int = 8
val maxInt: Int = Int.MAX_VALUE
val minInt: Int = Int.MIN_VALUE

val aLong: Long = 123131131324134324L
val maxLong: Long = Long.MAX_VALUE
val minLong: Long = Long.MIN_VALUE

val aShort: Short = 123
val maxShort: Short = Short.MAX_VALUE
val minShort: Short = Short.MIN_VALUE

val aFloat: Float = 12.9f
val maxFloat: Float = Float.MAX_VALUE
val minFloat: Float = -Float.MAX_VALUE

val aDouble: Double = 12.9
val maxDouble: Double = Double.MAX_VALUE
val minDouble: Double = -Double.MAX_VALUE


val aByte: Byte = 19
val maxByte: Byte = Byte.MAX_VALUE
val minByte: Byte = Byte.MIN_VALUE

val aChar: Char = 'a'

val aString: String = "aa"


fun typeofData() {

    println(aBoolean)

    println(anInt)
    println(maxInt)
    println(minInt)

    println(aShort)
    println(maxShort)
    println(minShort)

    println(aLong)
    println(maxLong)
    println(minLong)

    println(aFloat)
    println(maxFloat)
    println(minFloat)


    println(aDouble)
    println(maxDouble)
    println(minDouble)

    println(aByte)
    println(maxByte)
    println(minByte)


    println(aChar)

    println(aString)

}